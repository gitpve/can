package com.Util;

public class RadarInfo {

	public int left = 0;
	public int right = 0;
	public int left_midd = 0;
	public int right_midd = 0;
	public boolean bShow = false;

	public RadarInfo() {
		left = 0;
		right = 0;
		left_midd = 0;
		right_midd = 0;
		bShow = false;
	}

	public void clear() {
		left = 0;
		right = 0;
		left_midd = 0;
		right_midd = 0;
		bShow = false;
	}

}
